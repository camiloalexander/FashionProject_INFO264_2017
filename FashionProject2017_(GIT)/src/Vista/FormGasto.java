    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if(txtluz.getText().length()>0 || txtagua.getText().length()>0 || txtarriendo.getText().length()>0 || txtinternet.getText().length()>0 || txtotros.getText().length()>0){
            if(txtluz.getText().equals("")) txtluz.setText("0");
            if(txtagua.getText().equals("")) txtagua.setText("0");
            if(txtarriendo.getText().equals("")) txtarriendo.setText("0");
            if(txtinternet.getText().equals("")) txtinternet.setText("0");
            if(txtotros.getText().equals("")) txtotros.setText("0");
            guardar_o_modificar_gastos();
            txtluz.setText("");
            txtagua.setText("");
            txtarriendo.setText("");
            txtinternet.setText("");
            txtotros.setText("");
            accion="guardar";
            btnguardar.setText("Guardar");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "No ha ingresado gastos.");
        }
    }//GEN-LAST:event_btnguardarActionPerformed

