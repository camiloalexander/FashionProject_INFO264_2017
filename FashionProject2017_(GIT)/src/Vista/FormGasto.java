    void mostrarListaGastos(int mes, int año){
        try {
            DefaultTableModel modelo;
            Gastos gto=new Gastos();
            modelo = gto.mostrar(mes, año); 
            tabla.setModel(modelo);
            ocultar_columnas();
            totalmesaño=Integer.parseInt(gto.total.toString());

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }  
    
    void guardar_o_modificar_gastos(){               
        Gastos gto =new Gastos();
        gto.setLuz(Integer.parseInt(txtluz.getText())); 
        gto.setAgua(Integer.parseInt(txtagua.getText()));
        gto.setArriendo(Integer.parseInt(txtarriendo.getText()));
        gto.setInternet(Integer.parseInt(txtinternet.getText()));
        gto.setOtros(Integer.parseInt(txtotros.getText()));
        
        if (accion.equals("guardar")) {
            if (gto.ingresar(gto)) {
                JOptionPane.showMessageDialog(rootPane, "Gastos registrado satisfactoriamente.");
                mostrarListaGastos(0, 0);
            }
        }
        else if(accion.equals("editar")){
            gto.setId_Gastosmes(id_gasto);
            if (gto.modificar(gto)) {
                JOptionPane.showMessageDialog(rootPane, "Modificado satisfactoriamente.");
                mostrarListaGastos(0,0);
                txtluz.setText("");
                txtagua.setText("");
                txtarriendo.setText("");
                txtinternet.setText("");
                txtotros.setText("");
                accion="guardar";
                btnguardar.setText("Guardar");
            }
        }
    }
    
    void modificarGastos(int fila){
        btnguardar.setText("Guardar Modificación");
        //habilitar();
        accion="editar";
        Gastos gto = new Gastos();
        id_gasto = Integer.parseInt(tabla.getValueAt(fila, 0).toString());
        txtluz.setText(tabla.getValueAt(fila, 1).toString());
        txtagua.setText(tabla.getValueAt(fila, 2).toString());
        txtarriendo.setText(tabla.getValueAt(fila, 3).toString());
        txtinternet.setText(tabla.getValueAt(fila, 4).toString());
        txtotros.setText(tabla.getValueAt(fila, 5).toString());
        
        gto.setId_Gastosmes(id_gasto);
        gto.setLuz(Integer.parseInt(txtluz.getText()));
        gto.setAgua(Integer.parseInt(txtagua.getText()));
        gto.setArriendo(Integer.parseInt(txtarriendo.getText()));
        gto.setInternet(Integer.parseInt(txtinternet.getText()));
        gto.setOtros(Integer.parseInt(txtotros.getText()));
        
    }

